package models

type Product struct {
	ID       string    `json:"id,omitempty"`
	Name     string    `json:"name,omitempty"`
	Category *Category `json:"category,omitempty"`
	User     *User     `json:"user,omitempty"`
}
type Category struct {
	ID   string `json:"id,omitempty"`
	Name string `json:"name,omitempty"`
}
